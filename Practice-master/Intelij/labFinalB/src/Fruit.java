public class Fruit
{
    String name ;
    String variety ;
    int totalQuantityKg ;
    double pricePerKg ;

    public Fruit(String name, String variety, int totalQuantityKg, double pricePerKg)
    {
        this.name = name;
        this.variety = variety;
        this.totalQuantityKg = totalQuantityKg;
        this.pricePerKg = pricePerKg;
    }
}
